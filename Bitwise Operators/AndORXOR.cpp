#include <iostream>
using namespace std;
                    
int main() {
    
    int a = 4, b = 8;

    //AND operator
    cout << "And Operator: " << (a & b) << endl;

    //OR operator
    cout << "Or Operator: " << (a | b) << endl;

    //XOR operator
    cout << "Xor Operator: " << (a ^ b) << endl;

   return 0;
}